package ejemplosJAVABASICO;

public class DoWhile {

	public static void main(String[] args) {
		int num=1;
		System.out.println("Contando...");
		
		
		do{
			System.out.println("Vamos por el número "+num);
			num++;
		}while(num<=10);
		
		System.out.println("Fin de la cuenta");
	}
	

}
